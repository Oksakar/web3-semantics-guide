function addNumbers(firstNumber, secondNumber) {
  return firstNumber + secondNumber;
}
// Sample usage
addNumbers(1, 2); // 3
addNumbers(3, 2); // 5

function multiply(a, b) {
  return a * b;
}
// Sample usage
multiply(1, 2); // 2
multiply(2, 2); // 4

function double(x) {
  return x * 2;
}
// Sample usage
double(2); // 4
double(5); // 10

function nextDecade(year) {
  return year + 10;
}
// Sample usage
nextDecade(2020); // 2030
nextDecade(2000); // 2010

function nextYear(year) {
  return year + 1;
}
// Sample usage
nextYear(2000); // 2001
nextYear(2020); // 2021
